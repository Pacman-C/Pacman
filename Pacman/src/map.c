#include "../include/map.h"
#include <string.h>

void map_init(Map *map) {
    const char *layout[MAP_ROWS] = {
    "############################",
    "#............##............#",
    "#.####.#####.##.#####.####.#",
    "#O####.#####.##.#####.####O#",
    "#.####.#####.##.#####.####.#",
    "#..........................#",
    "#.####.##.########.##.####.#",
    "#.####.##.########.##.####.#",
    "#......##....##....##......#",
    "######.##### ## #####.######",
    "######.##          ##.######",
    "######.## ###--### ##.######",
    "######.## #      # ##.######",
    "######.## #      # ##.######",
    "T         #      #         T",
    "######.## #      # ##.######",
    "######.## ######## ##.######",
    "######.##          ##.######",
    "######.## ######## ##.######",
    "######.## ######## ##.######",
    "#............##............#",
    "#.####.#####.##.#####.####.#",
    "#.####.#####.##.#####.####.#",
    "#O..##................##..O#",
    "###.##.##.########.##.##.###",
    "###.##.##.########.##.##.###",
    "#......##....##....##......#",
    "#.##########.##.##########.#",
    "#.##########.##.##########.#",
    "#..........................#",
    "############################"
};

    map->pellet_count = 0;

    for (int y = 0; y < MAP_ROWS; y++) {
        for (int x = 0; x < MAP_COLS; x++) {
            map->grid[y][x] = layout[y][x];
            
            if (map->grid[y][x] == TILE_PELLET || map->grid[y][x] == TILE_POWER_PELLET) {
                map->pellet_count++;
            }
        }
    }
    map->total_pellets = map->pellet_count;
}

char get_tile(const Map *map, int x, int y) {
    if (x < 0 || x >= MAP_COLS || y < 0 || y >= MAP_ROWS) {
        return '#'; 
    }
    return map->grid[y][x];
}

void set_tile(Map *map, int x, int y, char tile) {
    if (x < 0 || x >= MAP_COLS || y < 0 || y >= MAP_ROWS) {
        return; 
    }
    map->grid[y][x] = tile;
}